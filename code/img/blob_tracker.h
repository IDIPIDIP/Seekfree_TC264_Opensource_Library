/*********************************************************************************************************************
 * 文件名称          blob_tracker.h
 * 功能简介          基于灰度图像的轻量级目标跟踪模块，适用于 MT9V03X 灰度摄像头
 *
 * 算法原理：
 *   1. 大津法(Otsu)自动二值化：遍历灰度直方图，找到使前景/背景类间方差最大的阈值。
 *      时间复杂度 O(W*H)，无需浮点运算。
 *   2. 质心跟踪(Centroid Tracking)：在感兴趣区域(ROI)内统计满足阈值条件的像素，
 *      计算其坐标质心和包围框，作为目标位置输出。
 *   3. ROI 预测：以上一帧包围框 + 搜索余量作为下一帧 ROI，缩小搜索范围，提高速度。
 *      若 ROI 内目标丢失，自动回退到全图搜索。
 *
 * 支持摄像头：MT9V03X（188×120 灰度，8 位/像素）
 * 参考资料：
 *   - Otsu, N. "A threshold selection method from gray-level histograms." (1979)
 ********************************************************************************************************************/

#ifndef BLOB_TRACKER_H
#define BLOB_TRACKER_H

#include "zf_common_headfile.h"
#include "zf_device_mt9v03x.h"

//================================================ blob_tracker 参数配置 ================================================
// 目标极性：跟踪比阈值暗的目标(BLOB_DARK)还是比阈值亮的目标(BLOB_BRIGHT)
#define BLOB_DARK           (0)                 // 跟踪暗目标（像素值 < 阈值）
#define BLOB_BRIGHT         (1)                 // 跟踪亮目标（像素值 >= 阈值）

// 有效目标的最少像素数量，过小则认为是噪声
#define BLOB_MIN_PIXEL_COUNT    (30)

// ROI 扩展余量（像素），基于上一帧包围框向外扩展，用于下一帧搜索
#define BLOB_SEARCH_MARGIN      (15)
//================================================ blob_tracker 参数配置 ================================================


//================================================ blob_tracker 结构体 ================================================
typedef struct
{
    uint16  cx;         // 目标质心 X 坐标
    uint16  cy;         // 目标质心 Y 坐标
    uint16  x_min;      // 包围框左边界
    uint16  x_max;      // 包围框右边界
    uint16  y_min;      // 包围框上边界
    uint16  y_max;      // 包围框下边界
    uint32  area;       // 匹配像素总数（近似目标面积）
} blob_result_struct;
//================================================ blob_tracker 结构体 ================================================


//================================================ blob_tracker 全局变量 ================================================
extern blob_result_struct blob_result;          // 最近一次跟踪结果
//================================================ blob_tracker 全局变量 ================================================


//================================================ blob_tracker 接口函数 ================================================
// 大津法自动计算最优二值化阈值
// 返回值：0-255 的最优阈值
uint8   blob_calc_otsu_threshold    (void);

// 灰度目标跟踪（基于 MT9V03X 图像）
// threshold  ：二值化阈值，建议使用 blob_calc_otsu_threshold() 获取，或手动设置固定值
// polarity   ：BLOB_DARK 跟踪暗目标 / BLOB_BRIGHT 跟踪亮目标
// result     ：输出的跟踪结果指针
// 返回值：1 表示找到有效目标，0 表示目标丢失
uint8   blob_track                  (uint8 threshold, uint8 polarity, blob_result_struct *result);

// 重置跟踪状态（目标丢失后手动调用，或初始化时调用）
void    blob_tracker_reset          (void);
//================================================ blob_tracker 接口函数 ================================================

#endif
