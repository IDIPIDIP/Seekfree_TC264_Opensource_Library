/*********************************************************************************************************************
 * 文件名称          mean_shift_tracker.h
 * 功能简介          基于 Mean Shift 算法的轻量级彩色目标跟踪模块，适用于 SCC8660 彩色摄像头
 *
 * 算法原理：
 *   Mean Shift 是一种无参数的迭代窗口移动算法：
 *   1. 以候选窗口内的像素颜色直方图与目标模板直方图的相似度（Bhattacharyya系数）为相似度量。
 *   2. 将每个像素按相似度加权，计算加权质心。
 *   3. 将窗口中心移向加权质心，反复迭代直到收敛（位移 < 1 像素或达到最大迭代次数）。
 *
 * 本实现针对 TC264 资源进行了以下简化：
 *   - 使用 HSL 色相（Hue）一维直方图（32 bins），内存占用极低（32 bytes）。
 *   - 窗口大小固定，不做尺度估计。
 *   - 整数运算为主，仅在 Bhattacharyya 系数计算时使用开方（isqrt 整数平方根）。
 *
 * 支持摄像头：SCC8660（160×120 RGB565，16 位/像素）
 * 参考资料：
 *   - Comaniciu, D.; Meer, P. "Mean Shift: A Robust Approach Toward Feature Space Analysis." (2002)
 ********************************************************************************************************************/

#ifndef MEAN_SHIFT_TRACKER_H
#define MEAN_SHIFT_TRACKER_H

#include "zf_common_headfile.h"
#include "zf_device_scc8660.h"

//================================================ mean_shift_tracker 参数配置 ================================================
#define MST_HIST_BINS       (32)        // 色相直方图 bin 数量（32 bins 对应每 bin 约 7.5° 色相）
#define MST_MAX_ITER        (8)         // 最大迭代次数
#define MST_CONV_THRESH     (1)         // 收敛阈值（像素），位移小于此值视为收敛
//================================================ mean_shift_tracker 参数配置 ================================================


//================================================ mean_shift_tracker 结构体 ================================================
typedef struct
{
    uint16  cx;                         // 跟踪窗口中心 X
    uint16  cy;                         // 跟踪窗口中心 Y
    uint16  win_w;                      // 跟踪窗口宽度
    uint16  win_h;                      // 跟踪窗口高度
    uint8   valid;                      // 1: 模板已初始化，可进行跟踪
} mst_state_struct;

typedef struct
{
    uint16  cx;                         // 目标质心 X
    uint16  cy;                         // 目标质心 Y
    uint16  win_w;                      // 当前窗口宽度
    uint16  win_h;                      // 当前窗口高度
    uint8   confidence;                 // 跟踪置信度 [0-100]，越高越可靠
} mst_result_struct;
//================================================ mean_shift_tracker 结构体 ================================================


//================================================ mean_shift_tracker 全局变量 ================================================
extern mst_state_struct  mst_state;     // 跟踪器内部状态
extern mst_result_struct mst_result;    // 最近一次跟踪结果
//================================================ mean_shift_tracker 全局变量 ================================================


//================================================ mean_shift_tracker 接口函数 ================================================
// 初始化目标模板：从 SCC8660 图像中指定矩形区域提取颜色直方图作为目标模型
// cx, cy     ：目标中心坐标（像素）
// win_w      ：目标窗口宽度（像素）
// win_h      ：目标窗口高度（像素）
void    mst_init_template           (uint16 cx, uint16 cy, uint16 win_w, uint16 win_h);

// 执行一帧的 Mean Shift 跟踪，更新 mst_result
// 返回值：1 表示跟踪成功（置信度 >= 阈值），0 表示目标丢失
uint8   mst_track                   (mst_result_struct *result);

// 重置跟踪器（目标丢失或重新初始化时调用）
void    mst_reset                   (void);
//================================================ mean_shift_tracker 接口函数 ================================================

#endif
